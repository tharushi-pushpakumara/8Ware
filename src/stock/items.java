    private void tblitemsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblitemsMouseClicked
        try {
            //JOptionPane.showMessageDialog(null,"here");
            int row = tblitems.getSelectedRow();
            //JOptionPane.showMessageDialog(null,"here too");
            String TC = (tblitems.getModel().getValueAt(row, 0).toString());
            // JOptionPane.showMessageDialog(null,"here then");
            ResultSet rs1 = JDBC.getdata("SELECT * FROM items WHERE itemCode ='" + TC + "'");
            //JOptionPane.showMessageDialog(null,"now here");


            if (rs1.next()) {
                //JOptionPane.showMessageDialog(null,"here");
                lbcode.setText(rs1.getString("itemCode"));

                txtqty.setText(rs1.getString("Quantity"));
                txtdes.setText(rs1.getString("Description"));
                txtprice.setText(rs1.getString("Purchased price"));
                txtrefno.setText(rs1.getString("Customer reference number"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblitemsMouseClicked

