    void itemCode() {
        try {
            ResultSet rs = JDBC.getdata("SELECT MAX(itemCode) FROM items");
            while (rs.next()) {
                String batch1 = rs.getString("MAX(itemCode)");
                int batch2 = Integer.parseInt(batch1.substring(3));
                int batch3 = (batch2 + 1);
                lbcode.setText("ITM" + batch3);
            }
        } catch (Exception e) {
            lbcode.setText("ITM" + 1001);
        }
    }

