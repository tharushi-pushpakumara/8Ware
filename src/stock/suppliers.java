    void supplierNo() {
        try {
            ResultSet rs = JDBC.getdata("SELECT MAX(sid) FROM suppliers");
            while (rs.next()) {
                String batch1 = rs.getString("MAX(sid)");
                int batch2 = Integer.parseInt(batch1.substring(3));
                int batch3 = (batch2 + 1);
                lbsn.setText("SUP" + batch3);
            }
        } catch (Exception e) {
            lbsn.setText("SUP" + 1001);
        }
    }
    
    
